#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	int i;

	i = 0;
	while (len - i > 0)
		if (!*src)
			dst[i++] = '\0';
		else
			dst[i++] = *src++;
	return (dst);
}
